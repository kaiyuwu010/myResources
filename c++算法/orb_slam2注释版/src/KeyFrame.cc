//更新共视图连接
void KeyFrame::UpdateConnections()
{
    //KFcounter存放关键帧-权重，权重为其它关键帧与当前关键帧共视地图点的个数，也称为共视程度
    map<KeyFrame*, int> KFcounter;
    //获得该关键帧的所有地图点
    vector<MapPoint*> vpMP;
    {
        unique_lock<mutex> lockMPs(mMutexFeatures);
        vpMP = mvpMapPoints;
    }
    //统计每一个地图点都有多少关键帧与当前关键帧存在共视关系，统计结果放在KFcounter
    for(vector<MapPoint*>::iterator vit=vpMP.begin(), vend=vpMP.end(); vit!=vend; vit++)
    {
        MapPoint* pMP = *vit;
        if(!pMP)
            continue;
        if(pMP->isBad())
            continue;
        //取出地图点的观测关键帧
        map<KeyFrame*, size_t> observations = pMP->GetObservations();
        //遍历观测关键帧
        for(map<KeyFrame*,size_t>::iterator mit=observations.begin(), mend=observations.end(); mit!=mend; mit++)
        {
            //跳过当前关键帧
            if(mit->first->mnId==mnId)
                continue;
            //存入容器，共视权重计数加1
            KFcounter[mit->first]++;
        }
    }
    //共视关键帧为空，返回
    if(KFcounter.empty())
        return;
    //声明变量记录最高的共视程度和对应关键帧指针
    int nmax=0;
    KeyFrame* pKFmax=NULL;
    //至少有15个共视地图点才会添加共视关系
    int th = 15;
    //vPairs记录与其它关键帧共视帧数大于th的关键帧
    vector<pair<int, KeyFrame*> > vPairs;
    vPairs.reserve(KFcounter.size());
    //找到对应权重最大的关键帧（共视程度最高的关键帧）
    for(map<KeyFrame*,int>::iterator mit=KFcounter.begin(), mend=KFcounter.end(); mit!=mend; mit++)
    {
        //比较，记录共视地图点更多的关键帧
        if(mit->second>nmax)
        {
            nmax=mit->second;
            pKFmax=mit->first;
        }
        //如果共视权重大于阈值
        if(mit->second>=th)
        {
            //存入vPairs
            vPairs.push_back(make_pair(mit->second,mit->first));
            //给该关键帧增加共视连接权重
            (mit->first)->AddConnection(this, mit->second);
        }
    }
    //如果没有超过阈值的权重，则对权重最大的关键帧建立连接
    if(vPairs.empty())
    {
        vPairs.push_back(make_pair(nmax, pKFmax));
        pKFmax->AddConnection(this, nmax);
    }
    //由大到小进行排序，sort函数默认升序排列
    sort(vPairs.begin(),vPairs.end());
    //将排序后的结果拆分存放到两种容器
    list<KeyFrame*> lKFs;
    list<int> lWs;
    for(size_t i=0; i<vPairs.size();i++)
    {
        lKFs.push_front(vPairs[i].second);
        lWs.push_front(vPairs[i].first);
    }
    //更新当前帧与其它关键帧的连接权重
    {
        unique_lock<mutex> lockCon(mMutexConnections);
        //更新当前帧共视关系容器
        mConnectedKeyFrameWeights = KFcounter;
        //更新从大到小排序的与当前帧共视的关键帧容器
        mvpOrderedConnectedKeyFrames = vector<KeyFrame*>(lKFs.begin(), lKFs.end());
        //更新从大到小排序的与当前帧共视的关键帧的权重容器
        mvOrderedWeights = vector<int>(lWs.begin(), lWs.end());
        //更新本质图生成树的连接
        if(mbFirstConnection && mnId!=0)
        {
            mpParent = mvpOrderedConnectedKeyFrames.front();
            mpParent->AddChild(this);
            mbFirstConnection = false;
        }
    }
}

